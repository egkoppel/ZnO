typedef float m4x4_t __attribute__((matrix_type(4,4)));

int main() {
	m4x4_t m1;
	m1[0][0] = 5;
	m1[0][1] = 6;
	m1[0][2] = 7;
	m1[0][3] = 8;
	m1[1][0] = 9;
	m1[1][1] = 1;
	m1[1][2] = 2;
	m1[1][3] = 3;
	m1[2][0] = 4;
	m1[2][1] = 10;
	m1[2][2] = 11;
	m1[2][3] = 12;
	m1[3][0] = 13;
	m1[3][1] = 14;
	m1[3][2] = 15;
	m1[3][3] = 16;
	m4x4_t m2 = m1 * 2;

	for (int i = 0; i < 4; i++) {
		printf("[ ");
		for (int j = 0; j<4; j++) {
			printf(" %f", m2[i][j]);
		}
		printf("]\n");
	}
}